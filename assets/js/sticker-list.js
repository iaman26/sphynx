$(document).ready(function() {
    const galleryContainer = $('.sticker-gallery');
    const imagesPerPage = 15;
    const totalPages = Math.ceil(imageDetails.length / imagesPerPage);

    // Function to display images for a specific page
    function displayImages(page) {
        galleryContainer.empty();
        // Clear previous images

        const startIndex = (page - 1) * imagesPerPage;
        const endIndex = startIndex + imagesPerPage;
        const pageImages = imageDetails.slice(startIndex, endIndex);

        pageImages.forEach(detail=>{
            const imageUrl = `cat-stickers/${detail.filename}`;
            const imgLink = $('<a>').attr('href', `sticker.php?img=${detail.filename}`).appendTo(galleryContainer);
            const img = $('<img loading="lazy">').attr('src', imageUrl).appendTo(imgLink);
        }
        );

        // Remove active class from all buttons
        $('.pagination-button').removeClass('active');
        // Add active class to the current button
        $(`.pagination-button[data-page="${page}"]`).addClass('active');
    }

    // Create a div to wrap pagination buttons
    const paginationWrapper = $('<div>').addClass('sticker-pagination');

    // Initial display (page 1)
    displayImages(1);

    // Add pagination buttons to the wrapper
    for (let i = 1; i <= totalPages; i++) {
        const pageButton = $('<button>').text(i).addClass('pagination-button').attr('data-page', i).on('click', function() {
            displayImages(i);
        });
        paginationWrapper.append(pageButton);
    }

    // Append the wrapper to the gallery container
    galleryContainer.after(paginationWrapper);

    $(`.pagination-button[data-page="1"]`).addClass('active');

    const homeGalleryContainer = $('.home-sticker-gallery');
    const limit = 15;
    let counter = 0;
    imageDetails.forEach(detail=>{
        if (counter >= limit) {
            return;
        }
        const imageUrl = `cat-stickers/${detail.filename}`;
        const imgLink = $('<a>').attr('href', `sticker.php?img=${detail.filename}`).appendTo(homeGalleryContainer);
        const img = $('<img loading="lazy">').attr('src', imageUrl).appendTo(imgLink);
        counter++;
    }
    );

});
